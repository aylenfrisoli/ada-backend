// Ejemplo: Verificar si una persona es mayor de edad

// Paso 1: Declaramos una variable para la edad de la persona
let edad = 18; // Puedes cambiar este valor para probar diferentes casos

// Paso 2: Usamos el operador ternario para determinar si es mayor de edad
// La condición es: ¿edad es mayor o igual a 18?
// Si es verdadera, el resultado será "Mayor de edad"
// Si es falsa, el resultado será "Menor de edad"
let mensaje = edad >= 18 ? "Mayor de edad" : "Menor de edad";

// Paso 3: Mostramos el mensaje en la consola
console.log(mensaje);

// Explicación:
// El operador ternario funciona como un "if" pero en una sola línea.
// Estructura: condición ? valor_si_verdadero : valor_si_falso
// En este caso:
// - condición: edad >= 18
// - valor_si_verdadero: "Mayor de edad"
// - valor_si_falso: "Menor de edad"
